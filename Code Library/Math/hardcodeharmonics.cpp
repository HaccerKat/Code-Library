const int blk = 3e5;
const int MX = 1e9;
const int sz = MX / blk + 1;
Mint temp[sz];
const Mint H[sz];
vector<Mint> getinvs(vector<Mint> &a) {
    int n = a.size();
    vector<Mint> res(n);
    res[0] = 1;
    for (int i = 1; i < n; i++) {
        res[i] = res[i - 1] * a[i - 1];
    }
    
    Mint inv = (Mint)1 / (res[n - 1] * a[n - 1]), cur = 1;
    for (int i = n - 1; i >= 0; i--) {
        res[i] *= inv * cur;
        cur *= a[i];
    }
    
    return res;
}

void precomp() {
    Mint cur = 0;
    temp[0] = 0;
    vector<Mint> a;
    for (int i = 1; i <= MX; i++) {
        a.push_back(i);
        if (i % blk == 0) {
            vector<Mint> invs = getinvs(a);
            a.clear();
            for (Mint x : invs) {
                cur += x;
            }
            
            temp[i / blk] = cur;
        }
    }
    
    cout << "{";
    for (int i = 0; i < sz; i++) {
        cout << temp[i] << (i == sz - 1 ? "}\n" : ", ");
    }
}

Mint getH(int n) {
    int amt = n / blk;
    Mint res = H[amt];
    vector<Mint> a;
    for (int i = amt * blk + 1; i <= n; i++) {
        a.push_back(i);
    }
    
    vector<Mint> invs = getinvs(a);
    for (Mint x : invs) {
        res += x;    
    }
    
    return res;
}